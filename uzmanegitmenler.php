        <p>  
            <h2>Tennur Yerlisu Lapa  </h2>

        1987 Dünya Tekvando Şampiyonu, 1982 ve 1984 Avrupa Tekvando Şampiyonudur. Cumhuriyet tarihinin bir spor dalında Dünya Şampiyonu olmuş ilk kadın sporcusudur.
        1966 yılında Almanya'nın Köln kentinde doğdu. Tekvandocu Gülnur Yerlisu ve Taner Bekir Yerlisu'nun ablasıdır. 
        11 yaşında tekvando eğitimine başladı. İlk eğitimi Kim Man Kuem'den aldı. Genç yaşta İstanbul ve Türkiye şampiyonlukları elde etti.
        Roma'da gerçekleşen 1982 Avrupa Tekvando Şampiyonası'nda 44 kiloda birinci oldu. 
        Bu başarısından ötürü Sedat Simavi Spor Ödülü'ne değer görüldü. 1984 yılında Stuttgart'ta düzenlenen Avrupa Tekvando Şampiyonası'nda ise 48 kiloda yarıştı, 
        ikinci kez Avrupa Şampiyonu oldu.
        1985 yılında İstanbul Üniversitesi'nde yükseköğrenimine başladı. Alman Dili ve Edebiyatı bölümünden 1989 yılında mezun oldu. 
        Üniversite yıllarında spor yaşamına devam etti. 1986 Avrupa Kupası'nda (İzmir) ve 1987'de Avrupa Kupası'nda (Girne) 51 kiloda şampiyon oldu.
        1987 yılında İspanya'nın başkenti Barselona'da düzenlenen Dünya Tekvando Şampiyonası'nda kadınlar 51 kiloda dünya şampiyonu oldu. 
        Milliyet gazetesinin 1987 Yılın Sporcusu Anketi sonucunda Naim Süleymanoğlu ile birlikte yılın sporcusu seçildi. Aktif spor yaşamı 1991 yılında sona erdi. 
        1992-1993 ve 2005 yılında Kadınlar Tekvando Milli Takımını çalıştırdı.
        Akademik kariyerine 1992'de Hacettepe Üniversitesi Spor Bilimleri ve Teknolojisi Yüksekokulu'nda araştırma görevlisi olarak başladı. 
        " Amatör sporda başarılı sporcuların ödüllendirilmesi" başlıklı tezi ile 1993 yılında Marmara Üniversitesi'nde yüksek lisans eğitimini, 
        "Gençlik ve Spor Genel Müdürlüğü'ndeki yönetim uygulamalarının yönetim süreçleri açısından değerlendirilmesi" başlıklı tezi ile 1999 yılında Gazi Üniversitesi'nde doktora eğitimini tamamladı. 
        Akademik yaşamına 2009 yılından itibaren Akdeniz Üniversitesi Beden Eğitimi ve Spor Yüksekokulu’nun Rekreasyon Bölümü'nde devam etti. 
        Serbest zaman katılımı, tatmini, engelleri ve serbest zamanda algılanan özgürlük konularında akademik çalışmaları vardır. 
        Kardeşleri ile birlikte aile şirketi olan spor malzemeleri şirketini yönetmektedir.
