            <h2>Ali ŞahinTekvando Milli Takımlar Teknik Direktörü Ali Şahin</h2>
         11 yaşında televizyonda izlediği dövüş filmlerine özenerek başladığı tekvandoda, 
         sporcularına kazandırdığı başarılarla Dünya Tekvando Federasyonu tarafından geçen yılın en iyi antrenörü seçildi.
         Olimpiyatta başarılı branşlarımızdan biri olan tekvandoda Türkiye’nin bu seviyelere gelmesinde büyük paya sahip Ali Şahin, 
         “Sportif faaliyetler sadece madalya kazanılan faaliyetler değildir. Ülke tanıtımına katkı, 
         takımın kazandığı başarılarla ülke temsilinde mükemmel başarı ortaya koyduk” dedi.
         14 yıllık aktif sporcu kariyerine antrenör olarak devam eden Ali Şahin, 2004’te milli takımın başına geçti.
         Teknik direktörlük kariyerinde 9 dünya şampiyonluğu bulunan Ali Şahin 3 olimpiyatta yer aldı ve 1’i altın 2 gümüş madalya başarısına imza attı.
